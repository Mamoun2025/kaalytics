    function getDimensionIcon(dimensionId, options = {}) {
        const iconName = dimensionIcons[dimensionId] || 'cube';
        return getSvgString(iconName, options);
    }

    // === GET DIMENSION ICON ELEMENT ===
    function getDimensionIconElement(dimensionId, options = {}) {
        const iconName = dimensionIcons[dimensionId] || 'cube';
        return createSvg(iconName, options);
    }

    // === LIST AVAILABLE ICONS ===
    function listIcons() {
        return Object.keys(icons);
    }

    // === CHECK IF ICON EXISTS ===
    function hasIcon(name) {
        return icons.hasOwnProperty(name);
    }

    // === PUBLIC API ===
    return {
        createSvg,
        getSvgString,
        getDimensionIcon,
        getDimensionIconElement,
        listIcons,
        hasIcon,
        dimensionIcons
    };

